#define TEMP_BUFFER_SIZE (1024*1024)
#define DM_PACKAGE_MAX_SIZE (2*1024)

typedef struct {
    unsigned char data[TEMP_BUFFER_SIZE];
    size_t head; // next write position
    size_t tail; // next read position
    size_t size; // current buffer size
} CircularBuffer;

void initBuffer(CircularBuffer *cb) {
    cb->head = 0;
    cb->tail = 0;
    cb->size = 0;
}

size_t writeBuffer(CircularBuffer *cb, unsigned char *buffer, size_t length) {
    size_t available = TEMP_BUFFER_SIZE - cb->size;
    //LOG_PRINT("cb->size= %d, available = %d , address = &p , \n", cb->size, available, cb->data);
    size_t toWrite = (length < available) ? length : available;
    size_t i;
    for (i = 0; i < toWrite; i++) {
        cb->data[cb->head] = buffer[i];
        //LOG_PRINT("0x%02X ", cb->data[cb->head]);
        cb->head = (cb->head + 1) % TEMP_BUFFER_SIZE; // update write position
    }
    cb->size += toWrite; // update current buffer size
    //LOG_PRINT("++++writeBuffer updatad cb->size = %d \n", cb->size);
    return toWrite;
}

size_t readBuffer(CircularBuffer *cb, unsigned char *buffer, size_t length) {
    size_t toRead = (length < cb->size) ? length : cb->size;
    size_t i;
    for (i = 0; i < toRead; i++) {
        if (buffer != NULL) {
            buffer[i] = cb->data[cb->tail];
        }
        cb->tail = (cb->tail + 1) % TEMP_BUFFER_SIZE;
    }
    cb->size -= toRead;
    //LOG_PRINT("----readBuffer updated cb->size = %d \n", cb->size);
    return toRead;
}


void printBuffer(CircularBuffer *cb) {
    size_t currentSize = cb->size;

    LOG_PRINT_D("Print Buffer contents: currentSize = %d \n", currentSize);
    size_t i;
    for (i = 0; i < cb->size; i++) {
        size_t index = (cb->tail + i) % TEMP_BUFFER_SIZE;
        LOG_PRINT_D("0x%02X ", cb->data[index]);
    }
    LOG_PRINT_D("\n");
}
