enum cls_wifi_fw_state {
	CLS_WIFI_FW_STATE_INIT = 0x494E4954,
	CLS_WIFI_FW_STATE_DONE = 0x1,		// fw file done in non-block mode
	CLS_WIFI_FW_STATE_DONE_BLOCK = 0x2,	// block done in block mode
	CLS_WIFI_FW_STATE_SUCCESS = 0x3,	// FW checking pass
	CLS_WIFI_FW_STATE_SUCCESS_ACK = 0x4,	// FW checking result ACK
	CLS_WIFI_FW_STATE_DONE_SECTION = 0x5,	// section done in block mode
	CLS_WIFI_FW_STATE_DONE_FILE = 0x6,	// fw file done in block mode
	CLS_WIFI_FW_STATE_DONE_AGC = 0x7,	// agc file done in non-block mode
	CLS_WIFI_FW_STATE_CREATE_PCIE_SHM = 0x8,	//create pcie shared_memory
	CLS_WIFI_FW_STATE_DONE_FINAL = 0xF,
	CLS_WIFI_FW_STATE_FAILED = 0xFF,	// an error occurred
	CLS_WIFI_FW_STATE_FAILED_HEADER = 0x5A84ED68,
};

