char *getNlCmdName(int cmd)
{
    return GET_NAME(cmd, nl_cmd_name);
}

void dumpNlMsg(struct nl_msg *msg, const char *title)
{
    int i;
    struct genlmsghdr *gnlh = nlmsg_data(nlmsg_hdr(msg));
    struct nlattr *tb[NL80211_ATTR_MAX + 1];

    nla_parse(tb, NL80211_ATTR_MAX, genlmsg_attrdata(gnlh, 0),
		  genlmsg_attrlen(gnlh, 0), NULL);

    DEBUG_INFO("%s dump cmd[%d:%s]\n", title, gnlh->cmd,
        GET_NAME(gnlh->cmd, nl_cmd_name));

    for (i=0;i<NL80211_ATTR_MAX;i++) {
        if (tb[i]) {
            DEBUG_INFO("\tattr: %s=", GET_NAME(i, nl_attr_name));
            switch (i) {
                case NL80211_ATTR_IFNAME:
                    DEBUG_INFO("%s", nla_get_string(tb[i]));
                    break;
                default:
                    break;
            }
            DEBUG_INFO("\n");
        }

    }

    DEBUG_INFO("%s dump end\n", title);
}

