#define CLS_BCNRPT_STR_LEN  64
#define CLS_BCNRPT_MACSTR_LEN 18
#define MAX_NUM_CLS_BCNRPT_CH 2
struct cls_bcnreport_req {
	char dest_mac[CLS_BCNRPT_MACSTR_LEN];
	int opclass;
	int chan;
	char bssid[CLS_BCNRPT_MACSTR_LEN];
	int interval;
	int duration;
	char mode[CLS_BCNRPT_STR_LEN];
	int detail;
	char ssid[CLS_BCNRPT_STR_LEN];
	uint8_t num_chans;
	uint8_t chans[MAX_NUM_CLS_BCNRPT_CH];
#define NUMS_BEACON_REQUESTED_EID 8
	uint8_t nums_eid;
	uint8_t eids[NUMS_BEACON_REQUESTED_EID];
	int last_beacon_rpt_ind;
};

struct cls_token_desc {
	const enum cls_token tok_id;
	const char *tok_text;
};

struct cls_cmd_param {
	enum cls_token key_tok;
	int val_pos;
	int val_len;
};

#define CLS_CMD_MAX_PARAM_COUNT		32

struct cls_cmd_request {
	struct cls_cmd_param param_tab[CLS_CMD_MAX_PARAM_COUNT];
	int param_count;
	const char *req_ptr;
	int req_len;
};

int cls_parse_params_encode_request(const char *params, char *buf_ptr, int buf_size);
int cls_init_cmd_request(struct cls_cmd_request *cmd_req, int cmd_tag,
		const unsigned char *req_ptr, int req_len);
int cls_get_value_text(const struct cls_cmd_request *cmd_req, enum cls_token tok,
		char *buf_ptr, int buf_size);
int cls_get_value_int(const struct cls_cmd_request *cmd_req, enum cls_token tok, int *value);
int cls_get_value_enable(const struct cls_cmd_request *cmd_req, enum cls_token tok, int *enable,
		int *conv_error);

int cls_validate_response_get_length(const char *buf_ptr, int buf_len);

int cls_handle_dpp_dev_action(struct cls_cmd_request *cmd_req, char *ifname,
				char *resp, int *resp_len);
void cls_macstr_to_array(char *macstr, uint8_t *macarray);
char *cls_data_to_array(void *data, uint8_t size);
void cls_data_to_hex(uint8_t *data, uint8_t size, char **hex_str);
