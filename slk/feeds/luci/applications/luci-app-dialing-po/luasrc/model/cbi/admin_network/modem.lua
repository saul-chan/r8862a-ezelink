--查询模块型号--
function modem_name(name)
	name=string.gsub(luci.sys.exec("uci -q get system.@system[0].module_name | grep "..name.." | wc -l"),"%s+","")
	return name
end

function modem_ver(name)
	name=string.gsub(luci.sys.exec("uci -q get system.@system[0].module_version | grep "..name.." | wc -l"),"%s+","")
	return name
end

function board_name(name)
	name=string.gsub(luci.sys.exec("ubus call system board | grep model | grep "..name.." | wc -l"),"%s+","")
	return name
end

